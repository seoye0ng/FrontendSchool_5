const test = [
  [1, 2, [10, 20]],
  [3, 4, [30, 40]],
  [5, 6, [50, 60]],
];

// 1.
for (const [i, j] of test) {
  console.log(i, j);
} //1 2 // 3 4 // 5 6

// 2.
for (const [i, j, k] of test) {
  console.log(i, j, k);
} //1 2 [10, 20] // 3 4 [30, 40] // 5 6 [50, 60]

// i, j, k, l, m, n
// 3.
for (const [i, j, [k, l]] of test) {
  console.log(i, j, k, l);
}

// 아래 코드는 기억할 것! //
