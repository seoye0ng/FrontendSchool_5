// * 배열의 전개구문
const 과일들 = ['사과', '파인애플', '수박'];
const 생선들 = ['조기', '갈치', '다금바리'];
const 합치면 = [...과일들, ...생선들];
const 합치면2 = [과일들, 생선들];

console.log(합치면); //['사과', '파인애플', '수박','조기', '갈치', '다금바리']
console.log(합치면2); // [Array(3), Array(3)]

// * 객체의 전개구문
